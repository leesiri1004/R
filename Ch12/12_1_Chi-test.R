#카이제곱 검정(chi-square-test)
# - 두 개의 변수가 서로 독립인지, 상관관계인지를 검정하는 통계기법

####################################################################
#주사위 적합도 검정
#주사위를 60번 던져서 나온 관측도수와 기대도수가 다음과 같을 때
#이 주사위를 특정게임에 사용하기에 적합한지를 검정

#주사위 눈 :  1,  2,  3,  4,  5,  6
#기대도수  : 10, 10, 10, 10, 10, 10
#관측도수  :  4,  6, 17, 15,  9,  9

#가설설정
#귀무가설 - 이 주사위는 게임 사용에 적합하지 않다.
#대립가설 - 이 주사위는 게임 사용에 적합하다.

result <- c(4, 6, 17, 15, 9, 9)
chisq.test(result)

#결론
#p-value가 0.02533, 유의수준 95%(0.05)보다 작기 때문에 귀무가설 기각
#따라서 이 주사위는 게임 사용에 적합하다.


