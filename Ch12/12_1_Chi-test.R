#지역에 따른 사람들의 성별분포의 관련성 검정

#1단계 - 가설설정
#귀무가설 : 지역에 따른 성별은 차이가 없다.
#대립가설 : 지역에 따른 성별은 차이가 있다.
sample <- read.csv('./file/sample.csv')
View(sample)

#2단계 - 성별과 지역에 따른 도수분포표
sample_table <- xtabs(~GENDER + AREA, data = sample)
sample_table

#3단계 - 검정통계량 계산
chisq.test(sample_table)

#4단계 - 계산된 통계량으로 검정
#결론
#p-value가 0.5276이므로 유의수준 95%(0.05)보다 크기 때문에
#귀무가설 채택 따라서 지역에 따른 성별은 차이가 없다.


